//  Это — то место, которое стоит настроить под свою площадку.
//  BANNED_PATTERNS — список регулярных выражений; если сообщение
//  совпало хоть с одним, оно блокируется, а автору начисляется
//  «страйк» (см. spamCheck в server.js).
//
//  Хотите более/менее строгую площадку — редактируйте только этот
//  файл. Пустой список ([]) полностью отключает фильтр по словам
//  (антифлуд и защита от повторов при этом продолжают работать).
// ================================================================
'use strict';

// Рекламные / запрещённые паттерны. Регэкспы ловят и часть опечаток/разрядок.
const BANNED_PATTERNS = [
  /заработок без вложений/i,
  /крипт\w* удво/i,
  /казино|вулкан|1xbet|1хбет|букмекер|ставки на спорт/i,
  /раздаём промокод|инвестиции под \d/i,
  /(bit\.ly|t\.me\/\+|cutt\.ly)/i,
  /разблокир\w* (инстаграм|instagram)/i,
  // наркотики / запрещённые вещества
  /\b(нарко|марихуан|гашиш|шишк[аи]|бошк|меф\b|мефедрон|амфетамин|герыч|героин|кокаин|гер[аы]|закладк|клад\b|клады|соль[а-я]* (микс|скорост)|спайс|экстази|лсд|lsd|шифер|колёс|колёса)\b/i,
  /(прод(ам|аю)|куплю|тема есть|тг для заказа).{0,30}(кл[ауо]д|закладк|шишк|меф|амф|гашиш|товар оптом)/i,
];

// Возвращает текст ошибки, если сообщение нарушает правила, иначе null.
function moderate(text) {
  if (!text) return null;
  const low = String(text).toLowerCase().replace(/\s+/g, ' ');
  for (const re of BANNED_PATTERNS) if (re.test(low)) return 'Сообщение нарушает правила (реклама/запрещёнка) и заблокировано';
  return null;
}

module.exports = { BANNED_PATTERNS, moderate };
